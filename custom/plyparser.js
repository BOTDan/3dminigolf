function parsePLY(fileContent) {
  const lines = fileContent.split("\n");
  if (lines.length === 0) {
    throw new Error("Not enough lines in file.")
  }
  if (lines[0] !== "ply") {
    throw new Error("File header 'ply' missing ast start of file.");
  }
  const elements = [];
  let currentLine = 1;
  while (lines[currentLine] !== 'end_header') {
    const parts = lines[currentLine].split(" ");
    switch (parts[0]) {
      case "comment": {
        break;
      }
      case "format": {
        if (parts[1] !== "ascii") {
          throw new Error("File is not in ASCII format.");
        }
        break;
      }
      case "element": {
        const name = parts[1];
        const amount = parseInt(parts[2]);
        elements.push({name, amount, format: []});
        break;
      }
      case "property": {
        const type = parts[1];
        const name = parts[parts.length-1];
        elements[elements.length-1].format.push({name});
        break;
      }
    }
    currentLine++;
  }
  currentLine++;
  const parsedData = {};
  elements.forEach((element) => {
    parsedData[element.name] = [];
    for (let i=0; i < element.amount; i++) {
      const parts = lines[currentLine+i].split(" ");
      switch (element.name) {
        case "vertex": {
          const pos = new Vector();
          element.format.forEach((property, index) => {
            switch (property.name) {
              case "x": pos.x = parseFloat(parts[index]); break;
              case "y": pos.y = parseFloat(parts[index]); break;
              case "z": pos.z = parseFloat(parts[index]); break;
            }
          });
          parsedData[element.name].push(pos);
          break;
        }
        case "face": {
          const verts = [];
          const amount = parts[0];
          for (let j=0; j < amount; j++) {
            verts.push(parseInt(parts[1+j]));
          }
          parsedData[element.name].push(verts);
          break;
        }
      }
    }
    currentLine += element.amount;
  });
  return parsedData;
}

function generateModel(parsedData) {
  const model = new Model();
  model.verts = parsedData.vertex;
  parsedData.face.forEach(verts => {
    const face = new Face(verts);
    face.flipNormal = true;
    model.addFace(face);
  });
  return model;
}

